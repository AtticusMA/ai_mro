// Generate specs/traceability.md from front-matter of all spec/plan/tasks/adr files.
import { readdirSync, statSync, writeFileSync } from 'node:fs'
import { join, relative, basename, dirname, sep } from 'node:path'
import { fileURLToPath } from 'node:url'
import { readFM } from './_frontmatter.mjs'

const SPECS_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')

function* walk(dir) {
  for (const entry of readdirSync(dir)) {
    const p = join(dir, entry)
    const s = statSync(p)
    if (s.isDirectory()) yield* walk(p)
    else if (s.isFile() && p.endsWith('.md')) yield p
  }
}
function rel(p) {
  return relative(SPECS_ROOT, p).split(sep).join('/')
}

const specs = []
const plans = new Set()
const tasks = new Set()
const adrs = []

for (const f of walk(SPECS_ROOT)) {
  const r = rel(f)
  if (r.startsWith('_templates/') || basename(f) === 'README.md' || r === 'traceability.md')
    continue
  const { fm } = readFM(f)
  if (!fm) continue
  if (basename(f).endsWith('.spec.md'))
    specs.push({ id: fm.id, title: fm.title, domain: fm.domain, status: fm.status, version: fm.version, rel: r })
  else if (basename(f).endsWith('.plan.md')) plans.add(fm.id)
  else if (basename(f).endsWith('.tasks.md')) tasks.add(fm.id)
  else if (r.startsWith('adr/'))
    adrs.push({ id: fm.id, title: fm.title, status: fm.status, date: fm.date, rel: r })
}

specs.sort((a, b) => String(a.id).localeCompare(String(b.id)))
adrs.sort((a, b) => String(a.id).localeCompare(String(b.id)))

const lines = []
lines.push('# Traceability Matrix')
lines.push('')
lines.push('> Auto-generated by `specs/_scripts/gen-traceability.mjs`. Do not edit by hand.')
lines.push('')
lines.push('## Specs')
lines.push('')
lines.push('| ID | 标题 | Domain | 状态 | 版本 | Plan | Tasks | 文件 |')
lines.push('|----|------|--------|------|------|------|-------|------|')
for (const s of specs) {
  lines.push(
    `| ${s.id} | ${s.title ?? ''} | ${s.domain ?? ''} | ${s.status ?? ''} | ${s.version ?? ''} | ${plans.has(s.id) ? 'Y' : '-'} | ${tasks.has(s.id) ? 'Y' : '-'} | [${s.rel}](${s.rel}) |`
  )
}
lines.push('')
lines.push('## ADRs')
lines.push('')
lines.push('| ID | 标题 | 状态 | 日期 | 文件 |')
lines.push('|----|------|------|------|------|')
for (const a of adrs) {
  lines.push(`| ${a.id} | ${a.title ?? ''} | ${a.status ?? ''} | ${a.date ?? ''} | [${a.rel}](${a.rel}) |`)
}
lines.push('')
lines.push(`Generated: ${new Date().toISOString()}`)

writeFileSync(join(SPECS_ROOT, 'traceability.md'), lines.join('\n') + '\n', 'utf8')
console.log(`[specs:traceability] wrote traceability.md (${specs.length} spec, ${adrs.length} adr).`)
